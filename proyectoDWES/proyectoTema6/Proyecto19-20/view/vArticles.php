        <div class="title">Chimborazo: Highest Above Earth's Center</div>
        <div class="description">
            <p>Chimborazo in Ecuador has an altitude of 6,310 meters (20,703 feet). Mount Everest has a higher altitude, and Mauna Kea is "taller." However, Chimborazo has the distinction of being the "highest mountain above Earth's center."</p>
            <p>This is because Earth is not a sphere - it is an oblate spheroid. As an oblate spheroid, Earth is widest at its equator. Chimborazo is just one degree south of the equator. At that location, it is 6,384 kilometers (3,967 miles) above Earth's center, or about 2 kilometers (about 1.2 miles) farther from Earth's center than Mount Everest.</p>
