        <div class="title">Mount Everest: Highest Altitude</div>
        <div class="description">
            <p>Almost everyone calls Mount Everest "the highest mountain in the world," and climbers from everywhere travel to Everest hoping to earn the distinction of climbing the "World's Highest."</p>
            <p>Mount Everest is called the world's highest mountain because it has the "highest elevation above sea level." We could also say that it has the "highest altitude."</p>
            <p>The peak of Mount Everest is 8,850 meters (29,035 feet) above sea level. No other mountain on Earth has a higher altitude. However, some mountains might be considered "taller" (with taller being "the total vertical distance between their base and their summit").</p>
