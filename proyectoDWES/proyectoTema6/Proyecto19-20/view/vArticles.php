        <div class="title">Mauna Kea: Tallest Mountain</div>
        <div class="description">
            <p>Mauna Kea has an altitude of 4,205 meters (13,796 feet) - much lower than Mount Everest. However, Mauna Kea is an island, and if the distance from the bottom of the nearby Pacific Ocean floor to the peak of the island is measured, then Mauna Kea is "taller" than Mount Everest.</p>
            <p>Mauna Kea is over 10,000 meters tall compared to 8,850 meters for Mount Everest - making it the "world's tallest mountain."</p>
